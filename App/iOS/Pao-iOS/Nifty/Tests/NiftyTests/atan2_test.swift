import XCTest
@testable import Nifty

class atan2_test: XCTestCase 
{
    #if os(Linux)
    static var allTests: [XCTestCaseEntry] 
    {
        let tests = 
        [
            testCase([("testBasic", self.testBasic)]),
        ]

        return tests
    }
    #endif

    func testBasic() 
    {        
        let tol = 1E-4
        let x = 0.5
        let y = 0.7
        let opxy = atan2(x, y)
        let ansxy = 0.6202
        XCTAssert(isequal(opxy, ansxy, within: tol), "\(opxy) != \(ansxy)")
    }
}
